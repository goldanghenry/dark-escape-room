export function getTagDescription(label: string): string {
  const key = label.trim()
  return (
    TAG_DESCRIPTIONS[key] ??
    TAG_DESCRIPTIONS[key.replace(/\s/g, '')] ??
    `「${key}」에 대한 토론에서 자주 다루는 개념입니다.`
  )
}

export function splitTagParts(tag: string): string[] {
  return tag
    .split('/')
    .map(s => s.trim())
    .filter(Boolean)
}
