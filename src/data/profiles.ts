export function calculateProfile(totalScores: Scores): Profile {
  const entries = Object.entries(totalScores) as [keyof Scores, number][]
  const sorted = entries.sort((a, b) => b[1] - a[1])
  const top1 = sorted[0][0]
  const top2 = sorted[1][0]

  const max = sorted[0][1]
  const min = sorted[sorted.length - 1][1]
  if (max - min <= 3) {
    return PROFILES.find(p => p.id === 'balanced')!
  }

  const match = PROFILES.find(
    p => p.dominant[0] === top1 && p.dominant[1] === top2
  )
  if (match) return match

  const fallback = PROFILES.find(p => p.dominant[0] === top1)
  return fallback ?? PROFILES[7]
}
